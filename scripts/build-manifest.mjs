import { readdir, stat, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const IMAGE_DIR = path.join(ROOT, 'images');
const OUT_FILE = path.join(ROOT, 'assets', 'data', 'images.js');
const IMAGE_EXTENSIONS = new Set(['.png', '.jpg', '.jpeg', '.webp']);

async function collectImages(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    if (entry.name.startsWith('.')) continue;

    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...await collectImages(fullPath));
      continue;
    }

    if (entry.isFile() && IMAGE_EXTENSIONS.has(path.extname(entry.name).toLowerCase())) {
      const info = await stat(fullPath);
      files.push({
        path: path.relative(ROOT, fullPath).split(path.sep).join('/'),
        file: entry.name,
        size: info.size,
      });
    }
  }

  return files;
}

const images = (await collectImages(IMAGE_DIR)).sort((a, b) => a.path.localeCompare(b.path, 'zh-Hans-CN'));
const totalBytes = images.reduce((total, image) => total + image.size, 0);
const generatedAt = new Date().toISOString();
const payload = images.map((image, index) => ({
  id: String(index + 1).padStart(3, '0'),
  ...image,
}));

const js = `/* Generated by scripts/build-manifest.mjs. Do not edit by hand. */
window.TRADITIONAL_COLOR_PROJECT = ${JSON.stringify({
  count: payload.length,
  totalBytes,
  generatedAt,
  archiveName: 'zhongguo-traditional-colors-images.zip',
}, null, 2)};

window.TRADITIONAL_COLOR_IMAGES = ${JSON.stringify(payload, null, 2)};
`;

await writeFile(OUT_FILE, js, 'utf8');
console.log(`Wrote ${payload.length} images to ${path.relative(ROOT, OUT_FILE)}`);
